from math import trunc

print(round(7.5))
print(int(7.5))
print(trunc(7.5))

# Round: acima de 5 arrendonda para cima, caso contrário para baixo. No próprio 5, arredondará para o número par mais próximo.
# Int:  arredonda para baixo até o número inteiro mais próximo.
# Trunc: trunca o número para apenas a parte inteira, removendo qualquer parte decimal.
